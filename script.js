// JavaScript for smooth scrolling and animations
document.addEventListener('DOMContentLoaded', function() {
    // Smooth scrolling for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Add animation to elements when they come into view
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };
    
    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate-in');
            }
        });
    }, observerOptions);
    
    // Observe sections for animation
    document.querySelectorAll('section').forEach(section => {
        observer.observe(section);
    });
    
    // Form submission
    const contactForm = document.getElementById('contactForm');
    if (contactForm) {
        contactForm.addEventListener('submit', function(e) {
            e.preventDefault();
            alert('Thank you for your message! I will get back to you soon.');
            contactForm.reset();
        });
    }
    
    // Add a simple console message to demonstrate JavaScript functionality
    console.log("Welcome to Oladokun Clement's portfolio website!");
    
    // Image slideshow functionality for hero section
    let currentImage = 1;
    setInterval(() => {
        const img1 = document.querySelector('.profile-img.img1');
        const img2 = document.querySelector('.profile-img.img2');
        
        if (currentImage === 1) {
            img1.style.opacity = 0;
            img2.style.opacity = 1;
            currentImage = 2;
        } else {
            img1.style.opacity = 1;
            img2.style.opacity = 0;
            currentImage = 1;
        }
    }, 7000);
});

// Add CSS for animation classes
